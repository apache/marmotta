package org.apache.marmotta.kiwi.exception;

/**
 * Thrown in case the requested database driver could not be found
 * <p/>
 * Author: Sebastian Schaffert
 */
public class DriverNotFoundException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	/**
     * Constructs a new exception with the specified detail message.  The
     * cause is not initialized, and may subsequently be initialized by
     * a call to {@link #initCause}.
     *
     * @param driver class name of the database driver that is missing
     */
    public DriverNotFoundException(String driver) {
        super("The database driver "+driver+" was not found in the classpath");
    }
}
